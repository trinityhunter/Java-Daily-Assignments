public class Ass4_Q2 {

    public static void main(String[] args) {

        for(int i=1; i<=5; i++){
            if(i%2==1){
                System.out.print((int)Math.pow(i, 3) + " ");
            }
            else{
                System.out.print((int)Math.pow(i, 2) + " ");
            }
        }

    }

}
